char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	int i;

	i = 0;
	while ((i <= n - 1 || src[i] == '\0'))
	{
		dest[i] = src[i];
		i++;
	}
	if (i > 0 && n > i)
	{
		dest[i] = '\0';
	}
	return (dest);
}
