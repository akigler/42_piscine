char	*ft_strcapitalize(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
			str[i] += ('a' - 'A');
		i++;
	}
	while (i >= 0)
	{
		if (i == 0 && str[i] >= 'a' && str[i] <= 'z')
			str[0] -= ('a' - 'A');
		if ((str[i] == ' ' || str[i] == '+' || str[i] == '-')
			&& str[i + 1] >= 'a' && str[i + 1] <= 'z')
		{
			str[i + 1] = str[i + 1] - ('a' - 'A');
		}
		i--;
	}
	return (str);
}
