int		sub(int a, int b)
{
	return (a - b);
}

int		add(int a, int b)
{
	return (a + b);
}

int		mult(int a, int b)
{
	return (a * b);
}

int		dev(int a, int b)
{
	return (a / b);
}

int		mod(int a, int b)
{
	return (a % b);
}